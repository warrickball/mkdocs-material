def function(a, b):
    """A docstring in the Google style.

    This is the description of what the function does.

    Args:
      a: float
        anything, really
      b: a second instance of something

    Returns:
      The sum of `a` and `b`, i.e. `a+b`.

    Raises:
      Nothing novel, but probably some sort of error if `a` and `b`
      can't be added.

    """
